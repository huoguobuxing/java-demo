package jdbc;

public class Utils {
    public static void outputStartSeparator(String name) {
        String str = "*************** start " + name + " ****************";
        System.out.println(str);
    }
    public static void outputEndSeparator(String name) {
        String str = "*************** end " + name + " ****************";
        System.out.println(str);
    }

}
